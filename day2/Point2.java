package day2;

public class Point2 {
    public int x;
    public int y;
    public int aim;

    public Point2() {
        this.x = 0;
        this.y = 0;
        this.aim = 0;
    }

    public Point2(int x, int y, int aim) {
        this.x = x;
        this.y = y;
        this.aim = aim;
    }

}
